first  = input("What is the first number? :")
second = input("What is the second number? :")

sum1 = int(first)
sum2 = int(second)
print("\n%d + %d = %d\n\
%d - %d = %d\n\
%d * %d = %d\n\
%d / %d = %d\n"
%(sum1, sum2, (sum1 + sum2),\
  sum1, sum2, (sum1 - sum2),\
  sum1, sum2, (sum1 * sum2),\
  sum1, sum2, (sum1 / sum2))	
	)
